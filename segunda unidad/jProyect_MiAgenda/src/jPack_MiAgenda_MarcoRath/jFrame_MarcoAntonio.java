    private void jbtn_pintarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_pintarActionPerformed
     int Fila, Columna;
        
        try{
            
            Fila = Integer.parseInt(jtxt_fila.getText());
            Columna = Integer.parseInt(jtxt_columna.getText());
            
             pintarCelda(jTable_misamigos, Fila, Columna, Color.GREEN);
            
        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null, "....Erorrrrrr....");
            
        }
    }                                           

     public static void pintarCelda(JTable tabla, int fila, int columna, Color color) {
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setBackground(color);
        tabla.getColumnModel().getColumn(columna).setCellRenderer(renderer);

        // Obtener la celda espec√≠fica y forzar su repintado
        Rectangle rect = tabla.getCellRect(fila, columna, true);
        tabla.repaint(rect);
    
    }//GEN-LAST:event_jbtn_pintarActionPerformed

