    private void jbtn_eliminarfilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarfilaActionPerformed
    //eliminar fila debe verificar que exista una fila seleccionada
    //getSelectRow devuelve un # este # es el de la fila seleccionada,al no exixstir una fila seleccionada debuelbe
    //-1
    
    int filaseleccionada=jTable_misamigos.getSelectedRow();
    //preguntar si la respuesta es diferente de -1(osea existe seleccion)
    if(filaseleccionada>-1){
    modelotabla=(DefaultTableModel) jTable_misamigos.getModel();
    modelotabla.removeRow(filaseleccionada);
    }
    else{//si es -1 no existe la fila
        JOptionPane.showMessageDialog(null,"Seleccione una fila");
        
    
    }
    }//GEN-LAST:event_jbtn_eliminarfilaActionPerformed

    private void jbtn_mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_mostrarActionPerformed
       //declarar variables auxiliares
        int fila;
       int columna;
       String valor;
       try{
       //obtener valores ingresados
       fila=Integer.parseInt(jtxt_fila.getText());
       columna=Integer.parseInt(jtxt_columna.getText());
       //obtener el modelo de la tabla 
       modelotabla=(DefaultTableModel) jTable_misamigos.getModel();
       //obtener el texto del campo(getValueAt)
       valor=(String) modelotabla.getValueAt(fila, columna);
       label_mostrarElementos.setText(valor);
       
       }
       catch(Exception e)
       {
       JOptionPane.showMessageDialog(null, "...ERROR..Filas o Columnas Incorrectas ");
       }   
           
    }//GEN-LAST:event_jbtn_mostrarActionPerformed

