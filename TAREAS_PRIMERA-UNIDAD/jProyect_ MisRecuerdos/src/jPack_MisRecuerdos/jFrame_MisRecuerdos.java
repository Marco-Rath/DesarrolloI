    private void jMenuItem1_cascadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1_cascadaActionPerformed
    JInternalFrame[] frames = jDesktopPane1_panelinterno.getAllFrames();
    int Vari1 = 0,Vari2 = 0,Acum = 30;
    for (JInternalFrame frame : frames) {
        frame.setLocation(Vari1, Vari2);
        Vari1 =Vari1+ Acum;
        Vari2 =Vari2+ Acum;
        if (Vari1+ frame.getWidth() > jDesktopPane1_panelinterno.getWidth()) {
            Vari1 = 0;
        }
        if (Vari2 + frame.getHeight() > jDesktopPane1_panelinterno.getHeight()) {
            Vari2 = 0;
        }
    }
      
       
    }//GEN-LAST:event_jMenuItem1_cascadaActionPerformed

