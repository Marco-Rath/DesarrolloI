    private void jMenuItem1_abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1_abrirActionPerformed
       JFileChooser Abrir=new JFileChooser(); 
       int Boton =Abrir.showOpenDialog(null);
       if(Boton==JFileChooser.APPROVE_OPTION){
       //ventana interna
       jInternalFrame_Imagenes VI=new jInternalFrame_Imagenes(); 
      //cambiar de tamaÃ±o
      VI.setResizable(true);
      //maximisar 
      VI.setMaximizable(true);
      //minimizar
      VI.setIconifiable(true);
      //cerrar
      VI.setClosable(true);
      //agregar la ventana interna IV al panel interno
      jDesktopPane1_panelinterno.add(VI); 
      //colocar la imagen en la ventana interna VI y mostrarla
      String Camino="";
      Camino=Abrir.getSelectedFile().toString();
      //colocar la imagen dentro del Label
      VI.SetImagen(Camino);
      //colocar la ruta o el camino en la imagen en la VI
      VI.setTitle(Camino);
      //hacer visible la VI
      VI.setVisible(true);
      
      
      
      
      
       
       }
    }//GEN-LAST:event_jMenuItem1_abrirActionPerformed

